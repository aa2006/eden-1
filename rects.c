double get_square_area(double s/*Side(length, width, height, whatever*/) {
	return s*s;
}

double get_cube_volume(double s) {
	return s*s*s;
}

double get_rect_area(double l, double w/*length, width*/) {
	return l*w;
}

double get_rectprism_vol(double l, double w, double h/*length, width, height*/) {
	return l*w*h;
}
